print("Olá! Essa é a Calculadora de Área de Quadrados do Alex")
r = float(input("Qual o comprimento de uma aresta do quadrado que quer descobrir a área em cm²?: "))
area = r * r
r2 = r*2
area2 = r2*r2
print(f"A área de um quadrado com uma aresta deste comprimento é de: {area}cm²")
print(f"O dobro desta área é: {area2}cm²")